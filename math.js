var price1 = 25;
var price2 = 35;
//add (+)
var total = price1 + price2;
console.log(total);                 //output: 60

//subtraction(-)
var total = price2 - price1;
console.log(total);                  //output: 10

//multiply (*)
var total = price1 * price2;
console.log(total);                 //output: 875

//divider(/)
var total = price1 / price2;
console.log(total);                 //output: 0.7142857142857143

//modulo)(%)
var total = price1 % price2;
console.log(total);                 //output: 10

//increment(++)
var total= price2++;
console.log(total);                 //output: 36

//decerement(--)
price2--;                           //output: 34
