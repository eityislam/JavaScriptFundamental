var promise ="I promise I will go home"

//convert lowercase
console.log(promise.toLowerCase());   //output: i promisss i will go home

//convert uppercase
console.log(promise.toUpperCase())   //output: I PROMISSS I WILL GO HOME

//find the word
console.log(promise.indexOf("will"))    //output: 12

//split or vag kora
console.log(promise.split('I'));        //OUTPUT: [ '', ' promise ', ' will go home' ]
